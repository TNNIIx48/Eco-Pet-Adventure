    private void calculatefoodActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calculatefoodActionPerformed
        String selectedFood = foodtype.getSelectedItem().toString();
        double efType;
        switch (selectedFood) {
            case "chicken":
            efType = 6.9;
            break;
            case "beef":
            efType = 27.0;
            break;
            case "pork":
            efType = 12.1;
            break;
            case "vegetable":
            efType = 2.0;
            break;
            default:
            efType = 0.0;
        }
        System.out.print(efType);
    }//GEN-LAST:event_calculatefoodActionPerformed

